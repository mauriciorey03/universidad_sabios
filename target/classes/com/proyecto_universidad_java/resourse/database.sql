CREATE TABLE CIUDAD (
    ID INT NOT NULL AUTO_INCREMENT,
    NOMBRE VARCHAR(255) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE DIRECCIONES (
    ID INT NOT NULL AUTO_INCREMENT,
    DIRECCION_VALOR VARCHAR(255) NOT NULL,
    PRIMARY KEY (ID)
);

CREATE TABLE PERSONAS (
    ID_PERSONA INT NOT NULL AUTO_INCREMENT,
    NOMBRE_PERSONA VARCHAR(255) NOT NULL,
    APELLIDO_PERSONA VARCHAR(255) NOT NULL,
    TELEFONO INT,
    FECHA_NACIMIENTO DATE NOT NULL, /*YYYY-MM-DD*/
    GENERO ENUM('M', 'F') NOT NULL,
    CLASE_DOCUMENTO ENUM('Cédula', 'Cédula de Extranjería', 'Número de Pasaporte', 'Tarjeta de Identidad') NOT NULL,
    NUMERO_DOCUMENTO INT NOT NULL,
    ID_CIUDAD INT NOT NULL,
    ID_DIRECCION INT NOT NULL,
    FOREIGN KEY (ID_CIUDAD) REFERENCES CIUDAD (ID),
    FOREIGN KEY (ID_DIRECCION) REFERENCES DIRECCIONES (ID),
    PRIMARY KEY (ID_PERSONA)
);

CREATE TABLE CARRERAS (
    ID_PROGRAMA INT NOT NULL AUTO_INCREMENT,
    NOMBRE_PROGRAMA VARCHAR(255) NOT NULL,
    CATEGORIA ENUM('Pregrado', 'Posgrado') NOT NULL,
    PRIMARY KEY (ID_PROGRAMA)
);

CREATE TABLE ESTUDIANTE_CARRERA (
    ID_ESTUDIANTE INT NOT NULL AUTO_INCREMENT,
    ID_PERSONA INT NOT NULL,
    ID_PROGRAMA INT NOT NULL,
    PRIMARY KEY (ID_ESTUDIANTE),
    FOREIGN KEY (ID_PERSONA) REFERENCES PERSONAS (ID_PERSONA),
    FOREIGN KEY (ID_PROGRAMA) REFERENCES CARRERAS (ID_PROGRAMA)
);

CREATE TABLE FACULTADES (
    ID_FACULTAD INT NOT NULL AUTO_INCREMENT,
    NOMBRE_FACULTAD VARCHAR(255) NOT NULL,
    PRIMARY KEY (ID_FACULTAD)
);

CREATE TABLE PROFESORES (
    ID_PROFESOR INT NOT NULL AUTO_INCREMENT,
    ID_PERSONA INT NOT NULL,
    ID_FACULTAD INT NOT NULL,
    PRIMARY KEY (ID_PROFESOR),
    FOREIGN KEY (ID_PERSONA) REFERENCES PERSONAS (ID_PERSONA),
    FOREIGN KEY (ID_FACULTAD) REFERENCES FACULTADES (ID_FACULTAD)
);

CREATE TABLE EDIFICIOS (
    ID_EDIFICIO INT NOT NULL AUTO_INCREMENT,
    NOMBRE_EDIFICIO VARCHAR(255) NOT NULL,
    PISOS_EDIFICIO INT NOT NULL,
    PRIMARY KEY (ID_EDIFICIO)
);

CREATE TABLE CURSO_MATERIA (
    ID_CURSO INT NOT NULL AUTO_INCREMENT,
    NOMBRE_CURSO VARCHAR(255) NOT NULL,
    GUIA_CURSO TEXT NOT NULL,
    PRIMARY KEY (ID_CURSO)
);

CREATE TABLE PERIODO (
    ID_PERIODO INT NOT NULL AUTO_INCREMENT,
    ANIO INT NOT NULL,
    SEMESTRE ENUM('1', '2') NOT NULL,
    PRIMARY KEY (ID_PERIODO)
);

CREATE TABLE SALON (
    ID_SALON INT NOT NULL AUTO_INCREMENT,
    VALOR_SALON VARCHAR(255) NOT NULL,
    CAPACIDAD_SALON INT NOT NULL,
    PISO_EDIFICIO INT NOT NULL,
    PRIMARY KEY (ID_SALON),
    FOREIGN KEY (PISO_EDIFICIO) REFERENCES EDIFICIOS (ID_EDIFICIO)
);

CREATE TABLE VALOR_MATRICULA (
    ID_VALOR_MATRICULA INT NOT NULL AUTO_INCREMENT,
    VALOR_CREDITO DECIMAL(10, 2) NOT NULL,
    ID_PROGRAMA INT NOT NULL,
    ID_PERIODO INT NOT NULL,
    PRIMARY KEY (ID_VALOR_MATRICULA),
    FOREIGN KEY (ID_PROGRAMA) REFERENCES CARRERAS (ID_PROGRAMA),
    FOREIGN KEY (ID_PERIODO) REFERENCES PERIODO (ID_PERIODO)
);

CREATE TABLE MATERIAS (
    ID_MATERIA INT NOT NULL AUTO_INCREMENT,
    NOMBRE_MATERIA VARCHAR(255) NOT NULL,
    CREDITOS INT NOT NULL,
    CANTIDAD_ESTUDIANTES INT NOT NULL,
    PRIMARY KEY (ID_MATERIA)
);

CREATE TABLE REGISTRO (
    ID_REGISTRO INT NOT NULL AUTO_INCREMENT,
    ID_ESTUDIANTE INT NOT NULL,
    ID_PERIODO INT NOT NULL,
    ID_MATERIA INT NOT NULL,
    PRIMARY KEY (ID_REGISTRO),
    FOREIGN KEY (ID_ESTUDIANTE) REFERENCES ESTUDIANTE_CARRERA (ID_ESTUDIANTE),
    FOREIGN KEY (ID_PERIODO) REFERENCES PERIODO (ID_PERIODO),
    FOREIGN KEY (ID_MATERIA) REFERENCES MATERIAS (ID_MATERIA)
);

CREATE TABLE HORARIO (
    ID_HORARIO INT NOT NULL AUTO_INCREMENT,
    DIA_CLASE VARCHAR(20) NOT NULL,
    HORA_INICIO TIME NOT NULL,
    HORA_FINAL TIME NOT NULL,
    PRIMARY KEY (ID_HORARIO)
);